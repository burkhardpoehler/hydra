import org.eclipse.e4.ui.workbench.IWorkbench;
import org.eclipse.swt.widgets.Shell;
import com.hydra.project.command.OpenSettingsCommand;
import com.hydra.project.model.MyTreeItem;
import com.hydra.project.parts.LogfileView;


public class OpenSettingsHandler {

	@Execute
	public void execute(IWorkbench workbench, @Named(IServiceConstants.ACTIVE_SELECTION)@Optional MyTreeItem myTreeItem,
			@Named(IServiceConstants.ACTIVE_SHELL) Shell shell ) {

			LogfileView.log(this + "OpenSettingsHandler läuft");
			OpenSettingsCommand.Action(shell,myTreeItem);
	}
}
